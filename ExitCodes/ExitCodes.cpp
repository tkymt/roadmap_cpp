// ExitCodes.cpp : このファイルには 'main' 関数が含まれています。プログラム実行の開始と終了がそこで行われます。
//

#include <iostream>
#include <cstdlib>

void some_function(int exitcode) {
	if (exitcode == 1) {
		std::cout << "An error occurred." << std::endl;
		std::exit(1);
	}
}

int main()
{
	int exitcode = 0;
	std::cin >> exitcode;

	if (exitcode == 1) {
		std::cout << "An error occurred." << std::endl;
		return 1;
	}

	if (exitcode == 2) {
		std::cout << "Another error occurred." << std::endl;
		return 2;
	}

	some_function(exitcode);

	return 0; // Successful execution
}

